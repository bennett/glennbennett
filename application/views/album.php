            <h4>About The Concert</h4>
            <p>
            I performed this concert in 2016. It turned out to be a pretty last-minute thing. I was asked to be an opening act for a performer coming into town to perform at the San Fernando Valley's Guitar Merchant. As the time drew near, I ended up getting pretty sick, and I told them I would have to cancel. They kept touching base with me, asking I could please come and play. On the day of the concert, I felt pretty good, but my voice was still pretty deep from having a cold. But I decided to go ahead at the last moment. You can hear it in my voice that I was a bit sick.
            It was great to get a chance to play my songs and explain a little bit about the new music genre I identified in 2003 called "Horse Whispering Music."

