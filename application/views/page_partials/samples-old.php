        <hr>  
<p>The song request system works by providing the audience with a list of nearly 200 songs from which they can choose. This extensive list ensures a wide variety of choices, catering to the diverse musical tastes of the audience. It allows for a more personalized experience, as audience members can hear their favorite songs during the show.</p>

