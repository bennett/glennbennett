            <h3>Equipment Requirements</h3>
        </div>
        <p>Glenn is entirely self-sufficient in terms of equipment, bringing his microphone and PA system. He is also capable of supplying his own power, eliminating any concerns about power limitations. He needs a level, dry area for setup and needs to be able to roll his equipment into the location. Any place that has handicapped access will work.</p>

        <div class="fancy-title title-dotted-border mt-4">
            <h3>Weather Restrictions for Outdoor Events</h3>
        </div>
        <p>For an outdoor event, there are certain weather restrictions you need to keep in mind. These restrictions apply to the period during which the event will be held and the time to set up. The following are the restrictions:</p>
        <ul>
            <li><strong>Temperature:</strong> The temperature should be above 55 degrees Fahrenheit and below 95 degrees Fahrenheit.</li>
            <li><strong>Wind:</strong> Wind speed should be less than 20 mph and gusts should be less than 40 mph.</li>
            <li><strong>Rain:</strong> The chance of rain should be less than 25%.</li>
        </ul>
